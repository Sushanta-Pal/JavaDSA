public class bitwiseoperator {
        public static void main(String[] args) {
            int  a = 4;
            System.out.println(a<<1);  //8
            System.out.println(a<<3);  //a*2^b
            System.out.println(a>>1);  //a/2
            System.out.println(a>>3);  //a/2^b

        }
    }
    
